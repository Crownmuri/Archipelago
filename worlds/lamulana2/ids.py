def get_item_name_from_id(item_id: ItemID) -> str:
    # Handle Ankh Jewels
    if 134 <= item_id <= 142:
        return "Ankh Jewel"
    
    # Handle Sacred Orbs
    if 104 <= item_id <= 113:
        return "Sacred Orb"
    
    # For other items, convert enum name to display name
    for name, member in ItemID.__members__.items():
        if member == item_id:
            # Remove trailing underscore from "None_"
            if name.endswith('_'):
                name = name[:-1]
            # Convert camelCase to spaced words
            import re
            name = re.sub(r'([a-z])([A-Z])', r'\1 \2', name)
            return name
    
    raise ValueError(f"No item name found for ItemID {item_id}")